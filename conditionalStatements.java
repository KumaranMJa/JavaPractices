public class conditionalStatements {
    public static void main(String[] args) {
         int total = 400 ;
         if(total > 400){
            System.out.println("A");
         }
         else if (total > 200){
            System.out.println("B");
         }
         else if (total > 100){
            System.out.println("C");
         }
         else {
            System.out.println("Fail");
         }
    }
}
