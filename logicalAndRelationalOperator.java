public class logicalAndRelationalOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 15 ;
        System.out.println(a<b);
        System.out.println(b==a);
        boolean c = true;
        boolean d = false;
        System.out.println(!c);
        System.out.println(c && d);
        System.out.println(c || d);
    }
}
